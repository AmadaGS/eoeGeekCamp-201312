package com.eoe.pre.textday02;
/**
 * 示例：自增、自减
 */
public class Test23 {
	public static void main(String[] args){
		int i=10;
		System.out.println("i++:"+i++);
		i=10;
		System.out.println("++i:"+(++i));
		i=10;
		System.out.println("i+=5:"+(i+=5));
		i=10;
		System.out.println("i--:"+i--);
		i=10;
		System.out.println("--i"+--i);
		i=10;
		System.out.println("i-5:"+(i-=5));
	}

}
